const STATE_MAP: Record<string, Record<string, LatLng>> = {
  NSW,
  ACT,
  VIC,
  QLD,
}

/**
 * Look up approximate lat/lng for an Australian suburb.
 * Returns null if no centroid found — caller should use a city fallback.
 */
export function getSuburbLatLng(suburb: string, state: string): LatLng | null {
  const stateData = STATE_MAP[state.toUpperCase()] ?? STATE_MAP['NSW']
  const exact = stateData[suburb]
  if (exact) return exact

  // Case-insensitive fallback
  const lower = suburb.toLowerCase()
  const key = Object.keys(stateData).find(k => k.toLowerCase() === lower)
  return key ? stateData[key] : null
}

